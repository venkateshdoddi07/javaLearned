package com.codegnan;

import java.util.Scanner;

public class PatternChristmasTree {
	//value 5 is permanatly provided to the height variable
	public static final int height=5;

	public static void main(String[] args) {
		Scanner scn =new Scanner(System.in);
//assigning width
		int width=5;
		int space=2*width;
		
		int x=1;
		for(int a=x;a<=height;a++) {
			
			for(int i=space;i>=x;i++ ) {
				
				System.out.println(" ");
			}
			
			for(int j=1;j<=j;j++) {
				
				System.out.println("* ");
			}
			
			
			
			
			
			
			
			
			
		}
		

	

scn.close();
	}

}
